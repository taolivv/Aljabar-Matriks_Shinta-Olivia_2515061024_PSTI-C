# Bagaimana dengan v=(1,2) dan w=(2,-1), apakah juga orthogonal?
v = np.array([1, 2])
w = np.array([2, -1])

bukti2 = np.dot(v, w)

if bukti2 == 0:
    print("v dan w juga orthogonal, karena hasil akhirnya:", bukti2)
else:
    print("v dan w bukan orthogonal karena hasil akhirnya:", bukti2)