import numpy as np

# buat vector i dengan elemen (1,0)
i = np.array([1, 0])
i

# dan vector j dengan elemen (0,1)
j = np.array([0, 1])
j

# buktikan bahwa vector i dan vector j itu orthogonal
bukti1 = np.dot(i, j)

if bukti1 == 0:
    print("Jadi i dan j adalah orthogonal karena secara matematisnya juga (1, 0) . (0, 1) = (1 x 0) + (0 x 1) =", bukti1)
else:
    print("i dan j bukan orthogonal karena hasil akhirnya", bukti1)

