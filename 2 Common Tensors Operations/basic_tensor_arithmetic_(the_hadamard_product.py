# buat matrix Y dengan ukuran 2x3 menggunakan numpy
Y = np.array([[20, 40, 50], [30, 60, 90]])
print(Y)

# buat matrix Y_pt dengan ukuran 2x3 menggunakan pytorch
Y_pt = pt.tensor([[20, 30, 40], [30, 60, 90]])
print(Y_pt)

# buat matrix Y_tf dengan ukuran 2x3 menggunakan tensorflow
Y_tf = tf.Variable([[20, 30, 40], [30, 60, 90]])
print(Y_tf)

# kalikan matrix X dengan Y
print(X * Y)

# kalikan matrix X_pt dengan Y_pt
print(X_pt * Y_pt)

# kalikan matrix X_tf dengan Y_tf
print(X_tf * Y_tf)