import numpy as np
import torch as pt
import tensorflow as tf

# buat matrix X dengan ukuran 2x3 menggunakan numpy
X = np.array([[10, 20, 30], [15, 20, 45]])
print(X)

# buat matrix X_pt dengan ukuran 2x3 menggunakan pytorch
X_pt = pt.tensor([[10, 20, 30], [15, 30, 45]])
print(X_pt)

# buat matrix X_tf dengan ukuran 2x3 menggunakan tensorflow
X_tf = tf.Variable([[10, 20, 30], [15, 30, 45]])
print(X_tf)

# lakukan perkalian matrix X, X_pt dan X_tf dengan suatu skalar
print(X * 2)
print("\n", X_pt * 2)
print("\n", X_tf * 2)

# lakukan penjumlahan matrix X, X_pt dan X_tf dengan suatu skalar
print(X + 10)
print("\n", X_pt + 10)
print("\n", X_tf + 10)

# lakukan pengurangan matrix X, X_pt dan X_tf dengan suatu skalar
print(X - 5)
print("\n", X_pt - 5)
print("\n", X_tf - 5)

